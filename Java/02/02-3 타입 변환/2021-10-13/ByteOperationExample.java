package sec03.exam03;

public class ByteOperationExample{
    public static void main(String[] args){
        // 10 + 20 한 결과값을 변수 result1의 초기값으로 저장
        byte result1 = 10 + 20;
        // 30을 출력
        System.out.println(result1);

        // 10을 변수 x의 초기값으로 저장
        byte x = 10;
        // 20을 변수 y의 초기값으로 저장
        byte y = 20;
        // x + y 한 결과값을 변수 result2의 초기값으로 저장
        int result2 = x + y;
        // 30 을 출력
        System.out.pirntln(result2);
    }
}