// String 타입

package sec02.exam05;

public class StringExample{
    public static void main(String[] args){
        // 문자열 "이의령"을 변수 name의 초기값으로 저장
        String name = "이의령";
        // 문자열 "학생"을 변수 job의 초기값으로 저장
        String job = "학생";
        // 이의령 을 출력
        System.out.println(name);
        // 학생 을 출력
        System.out.println(job);
    }
}