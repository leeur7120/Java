public class HelloOne{                             // Hello의 H가 대문자로 작성되어야 함(파일명과 대소문자가 동일).
    // 프로그램 실행 진입점     <-- 라인 주석
    public static void main(String[] args){     // String의 S가 대문자로 작성되어야 함.
        // 콘솔에 출력하는 실행문       <-- 라인 주석
        System.out.println("Hello, Java");      // System의 S가 대문자로 작성되어야 함.     끝에 세미콜론(;)을 붙여줌.
    } // end of main    <-- 라인 주석
} // end of class   <-- 라인 주석